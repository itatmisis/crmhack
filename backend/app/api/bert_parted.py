def get_bert_parted(sentiments: dict):
    word_attributions = sentiments['word_attributions']
    parted = [0]
    jdx = 0
    start_idx = 0
    part_size = len(word_attributions) // 3

    for idx, item in enumerate(word_attributions):
        if idx - start_idx > part_size:
            parted[jdx] /= idx - start_idx
            jdx += 1
            start_idx = idx
            parted.append(0)
        parted[jdx] += item[1]

    return parted

